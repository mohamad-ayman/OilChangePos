/** Map known English auth/API messages to Arabic. */
export function translateAuthMessage(message: string): string {
  const map: Record<string, string> = {
    'Invalid username or password.': messagesAr['auth.invalidCredentials']!,
    'Account is disabled.': messagesAr['auth.accountDisabled']!,
    'Unable to reach the server.': messagesAr['auth.network']!,
    'Login failed.': messagesAr['auth.loginFailed']!,
  }
  return map[message] ?? message
}
